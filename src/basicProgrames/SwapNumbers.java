package basicProgrames;

public class SwapNumbers {
	public static void main(String args[]) {
		int a=10, b=7;
		a=a+b;
		b=a-b;
		a=a-b;
		System.out.println("a= "+a);
		System.out.println("b= "+b);
		
		int d=23, e=50,c=0;
		c=d+e;
		d=c-d;
		e=c-e;
		System.out.println("d= "+d);
		System.out.println("e= "+e);
	}
}
